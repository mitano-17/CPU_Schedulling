public class Label {
    public int num;
    public int arr;
    public int burst;

    public Label(int a, int b, int c)
    {
        num=a;
        arr=b;
        burst=c;
    }

    public int getNum()
    {
        return num;
    }
    public int getArr()
    {
        return arr;
    }

    public int getBurst()
    {
        return burst;
    }
}
